from datetime import datetime

def get_days_from_today(date: str) -> int:
    """
    Розраховує кількість днів між заданою датою і поточною датою.

    Параметри:
    date (str): Дата у форматі 'РРРР-ММ-ДД'.
    
    Повертає:
    int: Кількість днів між поточною датою і заданою датою. Повертає -1 у разі некоректного формату дати.
    """
    try:
        # Отримуємо поточну дату, враховуючи тільки день
        today_date = datetime.today().date()

        # Перетворюємо рядок у об'єкт datetime
        date_obj = datetime.strptime(date, "%Y-%m-%d").date()

        # Обчислюємо різницю між поточною датою і заданою датою
        delta = today_date - date_obj

        # Повертаємо кількість днів як ціле число
        return delta.days

    except ValueError:
        # Якщо формат дати некоректний, повертаємо -1
        return -1

# Тестові випадки
print(get_days_from_today("2024-07-05"))  # Має повертати кількість днів від поточної дати до 5 липня 2024
print(get_days_from_today("2026-10-09"))  # Має повертати від'ємне значення, якщо дата в майбутньому
print(get_days_from_today("2021-10-09"))  # Має повертати кількість днів з минулого
